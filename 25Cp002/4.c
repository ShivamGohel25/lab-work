#include <stdio.h>
int main()
{
    int a,b;

    a = 25;
    b = 4;
    printf("the sum of the a and b is:%d\n", a+b);
    printf("the substraction of the a and b is:%d\n", a-b);
    printf("the multiplication of the a and b is:%d\n", a*b);
    printf("the division of the a and b is:%.2f\n", a/4.0);
    printf("the modulo of the a and b is:%d\n", a%b);
    return 0;
}
